    public static final String DEFULT_DATE_FORMAT = "yyyy-MM-dd HH:mm:ss";

    public static String getDateString(Date pDate){
        String lRetVal = null;
        SimpleDateFormat lObjFormat = new SimpleDateFormat(DEFULT_DATE_FORMAT);
        if(null != pDate){
            lRetVal = lObjFormat.format(pDate);
        }
        return lRetVal;
    }

    public static Date getDate(String pDateSt, String pInput){
        Date lRetVal = null;
        try {
            SimpleDateFormat lObjFormat = new SimpleDateFormat(pInput);
            if(null != pDateSt){
                lRetVal = lObjFormat.parse(pDateSt);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
        return lRetVal;
    }

    public static void setExpiryDate(Context pContext, Date lDate){
        String lDateSt = getDateString(lDate);;
        SharedPreferences lPref = pContext.getSharedPreferences("EXPIRY_DATE_PREF", Context.MODE_PRIVATE);
        SharedPreferences.Editor lEditor = lPref.edit();
        lEditor.putString("EXPIRY_DATE", lDateSt);
        lEditor.commit();
    }

    public static Date getExpiryDate(Context pContext){
        SharedPreferences lPref = pContext.getSharedPreferences("EXPIRY_DATE_PREF", Context.MODE_PRIVATE);
        String lDate = lPref.getString("EXPIRY_DATE", null);
        Date lRetVal = getDate(lDate, DEFULT_DATE_FORMAT);
        return lRetVal;
    }
